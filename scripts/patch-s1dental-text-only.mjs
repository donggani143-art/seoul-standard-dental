// ── DB 작업 ──────────────────────────────────────────────────────────────────
let db;
const run = (s, p = []) => new Promise((r, j) => db.run(s, p, function (e) { e ? j(e) : r(this); }));
const get = (s, p = []) => new Promise((r, j) => db.get(s, p, (e, x) => e ? j(e) : r(x)));

async function main() {
  db = new sqlite3.Database(DB_PATH);
  console.log(`[서울에스원치과 텍스트(문구)만 보강 — hospital_id=${HOSPITAL_ID}]`);

  const h = await get('SELECT id,name,slug FROM hospitals WHERE id=?', [HOSPITAL_ID]);
  if (!h || h.slug !== HOSPITAL_SLUG_GUARD) {
    console.error(`[ABORT] hospital_id=${HOSPITAL_ID} 가 ${HOSPITAL_SLUG_GUARD}이 아님: ${JSON.stringify(h)}`);
    process.exitCode = 1; db.close(); return;
  }
  console.log(`  대상 병원: ${h.name} (slug=${h.slug})\n`);

  let totalApplied = 0, totalSkipped = 0, totalWarn = 0;

  try {
    for (const [slug, pairs] of Object.entries(PATCHES)) {
      const row = await get('SELECT content FROM pages WHERE slug=? AND hospital_id=?', [slug, HOSPITAL_ID]);
      if (!row) {
        console.log(`  [SKIP] /${slug} 페이지 없음`);
        continue;
      }
      let content = row.content || '';
      const beforeLen = content.length;
      let applied = 0, skipped = 0, warn = 0;
      for (const [oldStr, newStr] of pairs) {
        if (content.includes(newStr)) { skipped++; continue; } // 이미 적용됨
        if (!content.includes(oldStr)) {
          console.log(`  [WARN] /${slug}: OLD 미발견 — "${oldStr.slice(0, 50).replace(/\n/g, ' ')}..."`);
          warn++;
          continue;
        }
        content = content.replace(oldStr, newStr); // 첫 매칭만 교체 (안전)
        applied++;
      }
      if (applied > 0) {
        await run(
          `UPDATE pages SET content=?, updated_at=CURRENT_TIMESTAMP WHERE slug=? AND hospital_id=?`,
          [content, slug, HOSPITAL_ID]
        );
        const afterLen = content.length;
        console.log(`  [/${slug.padEnd(20)}] 적용=${applied}  skip=${skipped}  warn=${warn}  (${beforeLen}b → ${afterLen}b)`);
      } else {
        console.log(`  [/${slug.padEnd(20)}] 변경 없음  skip=${skipped}  warn=${warn}`);
      }
      totalApplied += applied; totalSkipped += skipped; totalWarn += warn;
    }
    console.log(`\n[요약] 총 ${totalApplied}건 적용 · ${totalSkipped}건 skip(이미 적용) · ${totalWarn}건 경고(OLD 미발견)`);
    console.log('[완료] 텍스트 보강 종료');
  } catch (e) {
    console.error('[에러]', e.message);
    process.exitCode = 1;
  } finally {
    db.close();
  }
}

main();
